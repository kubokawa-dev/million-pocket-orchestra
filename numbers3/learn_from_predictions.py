# Evaluation helpers
# -----------------------------

def position_hits(actual: str, pred: str) -> int:
    """Count how many positions match between actual and predicted."""
    return sum(1 for a, b in zip(actual[:3], pred[:3]) if a == b)


# -----------------------------
# Get predictions from database
# -----------------------------

def get_latest_predictions(conn, actual_number: str) -> tuple:
    """
    Get the most recent ensemble predictions from the database.
    Returns (prediction_id, list of predicted 3-digit numbers).
    """
    cur = conn.cursor()
    
    # Get the latest ensemble prediction that matches the actual number
    cur.execute("""
        SELECT id, top_predictions, target_draw_number
        FROM numbers3_ensemble_predictions 
        WHERE actual_numbers = %s
        ORDER BY created_at DESC 
        LIMIT 1
    """, (actual_number,))
    
    row = cur.fetchone()
    if not row:
        # If not found, try to get the latest prediction without actual_numbers set
        cur.execute("""
            SELECT id, top_predictions, target_draw_number
            FROM numbers3_ensemble_predictions 
            WHERE actual_numbers IS NULL
            ORDER BY created_at DESC 
            LIMIT 1
        """)
        row = cur.fetchone()
    
    if not row:
        print("[learn] No predictions found in database.")
        return None, []
    
    pred_id, top_preds_json, target_draw = row
    top_preds = json.loads(top_preds_json) if top_preds_json else []
    
    # Extract numbers from predictions
    predictions = [pred['number'] for pred in top_preds if 'number' in pred]
    
    print(f"[learn] Loaded {len(predictions)} predictions from database (ID: {pred_id}, Draw: {target_draw})")
    return pred_id, predictions


# -----------------------------
# Main entry
# -----------------------------

def learn(actual_number: str, actual_draw_number: int = None):
    """
    Learn from actual result and update model.
    
    Args:
        actual_number: Actual 3-digit number (e.g., '729')
        actual_draw_number: Draw number (optional, will fetch latest if not provided)
    """
    actual_number = actual_number.strip()
    if not re.fullmatch(r"\d{3}", actual_number):
        raise ValueError("actual_number must be a 3-digit string like '729'")

    conn = get_conn()
    
    # Get predictions from database
    pred_id, predictions = get_latest_predictions(conn, actual_number)
    
    if not predictions:
        print("[learn] No predictions to learn from. Please run predict_ensemble.py first.")
        conn.close()
        return

    # Load/update model
    state = load_model_state(MODEL_PATH)
    state = update_state_with_event(state, actual_number, predictions)
    save_model_state(MODEL_PATH, state)

    # Evaluate predictions
    max_hit = -1
    top_match = None
    for num in predictions:
        h = position_hits(actual_number, num)
        if h > max_hit:
            max_hit = h
            top_match = num

    # Log into PostgreSQL
    ensure_tables(conn)
    sync_identity_sequence(conn, 'numbers3_model_events')
    sync_identity_sequence(conn, 'numbers3_predictions_log')
    cur = conn.cursor()
    
    # Get actual draw number if not provided
    if actual_draw_number is None:
        cur.execute("SELECT MAX(draw_number) FROM numbers3_draws")
        row = cur.fetchone()
        actual_draw_number = row[0] if row and row[0] else None

    preds_json = json.dumps(predictions, ensure_ascii=False)

    cur.execute(
        '''
        INSERT INTO numbers3_model_events(
            event_ts, actual_number, predictions, hit_exact, top_match, max_position_hits, notes
        ) VALUES (%s, %s, %s, %s, %s, %s, %s)
        ''',
        (
            now_iso(),
            actual_number,
            preds_json,
            1 if actual_number in predictions else 0,
            top_match,
            int(max_hit if max_hit >= 0 else 0),
            'learn_from_predictions v1'
        )
    )

    # Also record predictions individually
    ts = now_iso()
    for n in predictions[:10]:  # Top 10 predictions
        cur.execute(
            'INSERT INTO numbers3_predictions_log(created_at, source, label, number, target_draw_number) VALUES (%s, %s, %s, %s, %s)',
            (ts, 'ensemble', 'prediction', n, actual_draw_number)
        )

    # Update the ensemble prediction with actual result
    # (hit_status and hit_count are already set by manage_prediction_history.py update command)
    # This is just to log the model event

    conn.commit()
    conn.close()

    print('[learn] ✅ Updated model_state.json and logged training event.')
    print(f"[learn] Actual: {actual_number} | Predictions: {len(predictions)} | Max position hits: {max_hit} (top: {top_match})")
    print(f"[learn] Model events: {state['events']} | Updated: {state['updated_at']}")


if __name__ == '__main__':
    import sys
    if len(sys.argv) > 1:
        actual = sys.argv[1]
        draw_num = int(sys.argv[2]) if len(sys.argv) > 2 else None
        learn(actual, draw_num)
    else:
        print("Usage: python numbers3/learn_from_predictions.py <actual_number> [draw_number]")
        print("Example: python numbers3/learn_from_predictions.py 729")
