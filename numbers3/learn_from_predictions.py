def now_iso() -> str:
    return datetime.now(timezone.utc).isoformat()


def ensure_dirs(path: str):
    os.makedirs(os.path.dirname(path), exist_ok=True)


# -----------------------------
# PostgreSQL Setup
# -----------------------------

def get_conn():
    db_url = os.environ.get('DATABASE_URL')
    if db_url and '?schema' in db_url:
        db_url = db_url.split('?schema')[0]
    return psycopg2.connect(db_url)


def ensure_tables(conn: psycopg2.extensions.connection):
    cur = conn.cursor()
    cur.execute(
        '''
        CREATE TABLE IF NOT EXISTS numbers3_model_events (
            id SERIAL PRIMARY KEY,
            event_ts TEXT NOT NULL,
            actual_number TEXT NOT NULL,
            predictions TEXT NOT NULL,
            hit_exact INTEGER NOT NULL DEFAULT 0,
            top_match TEXT,
            max_position_hits INTEGER NOT NULL,
            notes TEXT
        );
        '''
    )
    cur.execute(
        '''
        CREATE TABLE IF NOT EXISTS numbers3_predictions_log (
            id SERIAL PRIMARY KEY,
            created_at TEXT NOT NULL,
            source TEXT NOT NULL,
            label TEXT,
            number TEXT NOT NULL,
            target_draw_number INTEGER
        );
        '''
    )
    # Attempt to ensure auto-increment identity on legacy tables
    try:
        cur.execute("ALTER TABLE numbers3_model_events ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY")
    except Exception:
        pass
    try:
        cur.execute("ALTER TABLE numbers3_predictions_log ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY")
    except Exception:
        pass
    conn.commit()


def sync_identity_sequence(conn: psycopg2.extensions.connection, table: str, id_col: str = 'id'):
    """Ensure the table's identity/sequence is set to at least MAX(id)."""
    cur = conn.cursor()
    try:
        cur.execute(
            f"""
            SELECT pg_get_serial_sequence(%s, %s)
            """,
            (table, id_col)
        )
        row = cur.fetchone()
        seq = row[0] if row else None
        if seq:
            cur.execute(f"SELECT COALESCE(MAX({id_col}), 0) FROM {table}")
            max_id = cur.fetchone()[0] or 0
            cur.execute("SELECT setval(%s, %s, true)", (seq, int(max_id)))
            conn.commit()
    except Exception:
        conn.rollback()


# -----------------------------
# Model state (per-position probabilities)
# -----------------------------

def load_model_state(path: str) -> Dict:
    if os.path.exists(path):
        with open(path, 'r', encoding='utf-8') as f:
            return json.load(f)
    # Initialize uniform probabilities for 3 positions
    return {
        'version': 1,
        'updated_at': None,
        'events': 0,
        'pos_probs': [[0.1 for _ in range(10)] for _ in range(3)],
    }


def save_model_state(path: str, state: Dict):
    state['updated_at'] = now_iso()
    ensure_dirs(path)
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(state, f, ensure_ascii=False, indent=2)


def normalize(vec: List[float]) -> List[float]:
    s = sum(vec)
    if s <= 0:
        return [1.0/len(vec) for _ in vec]
    return [v / s for v in vec]


def update_state_with_event(state: Dict, actual: str, predictions: List[str]) -> Dict:
    """
    Update model state based on actual result and predictions.
    
    Args:
        state: Current model state
        actual: Actual 3-digit number (e.g., '729')
        predictions: List of predicted 3-digit numbers
    """
    # weights
    beta = 0.3  # update strength for new evidence
    w_actual = 1.0
    # predictions ranked by appearance: earlier is stronger
    pred_weights = [0.5 * (0.7 ** i) for i in range(len(predictions))]

    # per-position target distributions
    targets = [[1e-3 for _ in range(10)] for _ in range(3)]  # smoothing for 3 positions

    # reinforce predictions (small)
    for rank, num in enumerate(predictions):
        w = pred_weights[rank] if rank < len(pred_weights) else 0.1
        for i, ch in enumerate(num[:3]):
            d = ord(ch) - 48
            if 0 <= d <= 9:
                targets[i][d] += 0.2 * w

    # reinforce actual (strong)
    for i, ch in enumerate(actual[:3]):
        d = ord(ch) - 48
        if 0 <= d <= 9:
            targets[i][d] += w_actual

    # blend with existing state
    for i in range(3):  # 3 positions for Numbers3
        tgt = normalize(targets[i])
        old = state['pos_probs'][i]
        new = [ (1.0 - beta) * old[d] + beta * tgt[d] for d in range(10) ]
        state['pos_probs'][i] = normalize(new)

    state['events'] = int(state.get('events', 0)) + 1
    return state


